            <h2>
              
              Romblon
             </h2>
           </div>
           <p>
             Romblon is an archipelagic province of the Philippines located in the Mimaropa region. Its main islands include Tablas, the largest, which covers nine
              municipalities, Sibuyan with its three towns, as well as the smaller island municipalities of Corcuera, Banton, Concepcion, San Jose, and Romblon, the 
              provincial capital. The province lies south of Marinduque and Quezon, east of Oriental Mindoro, north of Aklan and Capiz, and west of Masbate. According 
              to the 2015 census, it has a total population of 292,781.
             <br>
             <br>
              Romblon has been inhabited by aboriginal Filipinos prior to the arrival of the Spanish in 1569. Archaeological artifacts recovered by the National Museum 
              in 1936 indicate that the aborigines of Romblon already have a rich and advanced culture. Under Spanish colonial rule, Romblon was initially administered 
               under the newly established province of Arevalo, until 1716, when it was transferred to the jurisdiction of the newly created province of Capiz. With  
               arrival of the Americans in 1901, Romblon was declared a province and placed under civilian rule. It lost its provincial status for a short while between 
                1907 and 1945, but regained it in 1946, just after World War II.
                <br>
             <br>
             The inhabitants of Romblon are divided into three ethnolinguistic groups: Romblomanon, Onhan and Asi. These groups occupy specific islands in the province and 
             have their own language and customs. Romblomanon is mainly spoken in the town of Romblon, in all of three towns of Sibuyan Island, and the towns of San Andres 
             and San Agustin in Tablas Island. Onhan is mainly spoken in the municipalities in the southern part of Tablas Island Alcantara, Looc, Ferrol, Santa Fe and Santa 
             Maria well as in the island municipality of San Jose. The northwestern part of Tablas Island  in Odiongan and Calatrava, as well as the islands municipalities of
              Corcuera, Banton, and Concepcion, speak the Asi language.
              <br>
              <br>
              Currently, the province relies on agriculture, particularly rice and copra farming as well as fishing, for its livelihood. It also has a lucrative marble industry
              due to an abundance of Italian-quality marble, hence, its moniker as the "Marble Capital of the Philippines." In recent years, 
              the province has also become an ecotourism destination, with several white sand beaches, diving spots, mountains and rainforests that tourists visit annually.
 
         </p>
